-- Учителя
CREATE TABLE public.teachers (
    teacher_id SERIAL PRIMARY KEY,
    last_name VARCHAR(50) NOT NULL,
    first_name VARCHAR(50) NOT NULL,
    middle_name VARCHAR(50),
    salary INTEGER CHECK (salary > 0),
    weekly_hours INTEGER CHECK (weekly_hours > 0)
);

-- Классы
CREATE TABLE public.classes (
    class_id SERIAL PRIMARY KEY,
    class_number VARCHAR(10) NOT NULL,
    student_count INTEGER CHECK (student_count >= 0),
    classroom INTEGER,
    homeroom_teacher_id INTEGER REFERENCES teachers(teacher_id)
);

-- Ученики
CREATE TABLE public.students (
    student_id SERIAL PRIMARY KEY,
    last_name VARCHAR(50) NOT NULL,
    first_name VARCHAR(50) NOT NULL,
    middle_name VARCHAR(50),
    birth_date DATE,
    gender VARCHAR(1) CHECK (gender IN ('М', 'Ж')),
    class_id INTEGER REFERENCES classes(class_id)
);

-- Предметы
CREATE TABLE public.subjects (
    subject_id SERIAL PRIMARY KEY,
    subject_name VARCHAR(50) NOT NULL,
    teacher_id INTEGER REFERENCES teachers(teacher_id),
    weekly_hours INTEGER CHECK (weekly_hours > 0)
);

-- Уроки/расписание
CREATE TABLE public.lessons (
    lesson_id SERIAL PRIMARY KEY,
    class_id INTEGER REFERENCES classes(class_id),
    subject_id INTEGER REFERENCES subjects(subject_id),
    classroom INTEGER,
    day_of_week VARCHAR(50) NOT NULL,
    lesson_time TIME NOT NULL,
    teacher_id INTEGER REFERENCES teachers(teacher_id)
);

-- Отдельные оценки
CREATE TABLE public.grades (
    grade_id SERIAL PRIMARY KEY,
    student_id INTEGER REFERENCES students(student_id),
    subject_id INTEGER REFERENCES subjects(subject_id),
    grade INTEGER CHECK (grade BETWEEN 2 AND 5),
    grade_date DATE NOT NULL,
    grade_type VARCHAR(20), -- 'контрольная', 'самостоятельная', 'устный ответ', 'домашнее задание'
    teacher_id INTEGER REFERENCES teachers(teacher_id),
    comment TEXT
);

-- Итоговые результаты по периодам
CREATE TABLE public.student_progress (
    progress_id SERIAL PRIMARY KEY,
    student_id INTEGER REFERENCES students(student_id),
    subject_id INTEGER REFERENCES subjects(subject_id),
    period_type VARCHAR(20) NOT NULL, -- 'четверть', 'полугодие', 'год'
    period_number INTEGER, -- номер четверти/полугодия
    academic_year VARCHAR(9), -- '2024-2025'
    average_grade NUMERIC(3,2) CHECK (average_grade >= 2.00 AND average_grade <= 5.00),
    final_grade INTEGER CHECK (final_grade BETWEEN 2 AND 5),
    UNIQUE(student_id, subject_id, period_type, period_number, academic_year)
);

-- Индексы для производительности
CREATE INDEX idx_students_class ON students(class_id);
CREATE INDEX idx_lessons_datetime ON lessons(lesson_date, lesson_time);
CREATE INDEX idx_grades_student_subject ON grades(student_id, subject_id);
CREATE INDEX idx_grades_date ON grades(grade_date);
CREATE INDEX idx_progress_student_period ON student_progress(student_id, period_type, academic_year);


