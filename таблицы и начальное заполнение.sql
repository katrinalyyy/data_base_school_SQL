-- Проверяем созданное расписание
SELECT 
    l.day_of_week,
    l.lesson_time,
    c.class_number,
    s.subject_name,
    CONCAT(t.last_name, ' ', t.first_name, ' ', t.middle_name) as teacher_name,
    l.classroom
FROM lessons l
JOIN classes c ON l.class_id = c.class_id
JOIN subjects s ON l.subject_id = s.subject_id
JOIN teachers t ON l.teacher_id = t.teacher_id
WHERE c.class_number IN ('10А', '10Б')
ORDER BY c.class_number, 
    CASE l.day_of_week 
        WHEN 'Понедельник' THEN 1
        WHEN 'Вторник' THEN 2
        WHEN 'Среда' THEN 3
        WHEN 'Четверг' THEN 4
        WHEN 'Пятница' THEN 5
        WHEN 'Суббота' THEN 6
    END, 
    l.lesson_time;